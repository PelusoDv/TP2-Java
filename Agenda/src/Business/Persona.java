
package Business;

public class Persona {
    private String nombre;
    private String apellido;
    private int dni;
    private String direc;
    private int telef;
    private String nac;
 
    public Persona(String nombre, String apellido, int dni, String direc, int telef, String nac) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.dni = dni;
        this.direc = direc;
        this.telef = telef;
        this.nac = nac;
    }
    
}
